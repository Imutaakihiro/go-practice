// =============================================================================
// 10_hello_web.go — 最小のWebサーバー(Loop 9+10 合体課題)
// =============================================================================
//
// ■ 全体像: 受付業務のメタファー
//
//   お客さん(ブラウザ)
//       │
//       ▼
//   ┌─────────── お店(Goサーバー) ───────────┐
//   │                                          │
//   │  ①開店係(ListenAndServe)                │
//   │     「:8080番窓口で営業開始!」           │
//   │                                          │
//   │  ②案内板(HandleFunc)                    │
//   │     "/"の相談 → helloHandler             │
//   │                                          │
//   │  ③対応係員(helloHandler)                │
//   │     お客さんと実際にやり取り             │
//   └──────────────────────────────────────────┘
//
// 3人の登場人物:
//   開店係   = ListenAndServe  「営業始めます!」と窓口を開ける
//   案内板   = HandleFunc      「どの相談は誰が対応」を決める表
//   対応係員 = helloHandler    実際にお客さんと話す
//
// 動かし方:
//   ターミナル1: go run ./learning/basics/examples/10_hello_web.go
//     → "サーバー起動中: ..." が出て、プロンプトが戻らない(=待機中)
//   ブラウザ  : http://localhost:8080 を開く
//     → "Hello, ご主人様!" が表示される
//   止め方    : ターミナル1で Ctrl + C
//
// =============================================================================

package main

// -----------------------------------------------------------------------------
// import: 使う道具箱の宣言
// -----------------------------------------------------------------------------
//   "fmt"      = 画面出力と文字列整形の道具箱(いつものやつ)
//   "net/http" = Webサーバー/クライアントの道具箱(標準ライブラリの傑作)
//
// 階層構造: net/http = "net" 道具箱の中の "http" サブ道具箱
// → encoding/json, io/ioutil など、階層が深い道具箱もある
import (
	"fmt"
	"net/http"
)

// -----------------------------------------------------------------------------
// A. 対応係員 helloHandler
// -----------------------------------------------------------------------------
//
// Webサーバーの対応係員は「必ずこの形」でないといけない(Goのルール)
//
//	┌─ シグネチャ契約 ─────────────────────────────┐
//	│ 第1引数: http.ResponseWriter 型              │
//	│ 第2引数: *http.Request 型                    │
//	│ 戻り値 : なし                                 │
//	└───────────────────────────────────────────────┘
//
// この契約に合ってないと http.HandleFunc が受け取ってくれない。
//
// ■ 引数 w http.ResponseWriter
//
//	w = 名前(自由、慣習で writer の w)
//	http.ResponseWriter = "返信用紙" という型
//
//	→ Goが係員に白紙を渡す。「お客さんに返す内容をここに書いてね」
//	→ 係員は「書くだけ」。郵送(レスポンス送信)は Goのランタイムの仕事
//	→ 「書く」と「届ける」が分離されてる = 係員は本業(何を書くか)に集中できる
//
// ■ 引数 r *http.Request
//
//	r = 名前(慣習で request の r)
//	*http.Request = "リクエストの住所" という型(ポインタ)
//
//	→ お客さんの申込書(URL,ヘッダ,Body,クッキーなど全部入り)
//	→ * がつく理由: Requestはデータ量が多い → 住所だけ渡す方がお得
//	→ Loop 3 で学んだ「変えたい時はポインタ」の別パターン
//	   → 「でかい時もポインタ」という使い道
//
// ■ 関数の中身
//
//	fmt.Fprintln(w, "...") = 返信用紙 w に文字列を書き込む
//
//	Println と Fprintln の違い:
//	  fmt.Println("...")       → ターミナル(標準出力)に書く
//	  fmt.Fprintln(w, "...")   → 第1引数で指定した書き込み先に書く
//
//	F = File(書き込み先)の F
//	→ 「書く動作」と「書く先」を分離する設計
//	→ 1つの関数でターミナル/ファイル/HTTPレスポンス、どこにでも書ける
//	→ Go標準ライブラリの統一感の出どころ
//
// ■ 書いた内容が届くまでの流れ
//
//	①この行で w に "Hello, ご主人様!\n" が書かれる
//	②helloHandler が終了
//	③Goのランタイムが w の中身を HTTPレスポンスとして送信
//	④ブラウザに届いて表示
func helloHandler(w http.ResponseWriter, r *http.Request) {
	fmt.Fprintln(w, "Hello, ご主人様!")
}

// -----------------------------------------------------------------------------
// main: サーバー起動の段取り
// -----------------------------------------------------------------------------
func main() {
	// =============================================================
	// C. 案内板に登録: http.HandleFunc
	// =============================================================
	//
	// 「案内板にこう書いて貼る」作業:
	//   ┌─ 案内板 ─────────────────┐
	//   │ "/"の相談 → helloHandler │
	//   └──────────────────────────┘
	//
	// ■ 引数
	//   第1引数 "/"          = URLパターン(ドアの表札)
	//   第2引数 helloHandler = 取り次ぐ係員の連絡先
	//
	// ■ 【超重要】関数名のあとに () がない理由
	//
	//   helloHandler()  = 今すぐ実行して結果を渡す
	//   helloHandler    = 関数そのもの(引き換え券)を渡す = 「あとで呼んでね」
	//
	//   日常の例え:
	//     helloHandler() → 「今すぐ田中さん呼んで客を対応させる」
	//     helloHandler   → 「田中さんの内線番号を案内板に貼る」
	//
	//   http.HandleFunc がほしいのは「内線番号」。
	//   なので () なしで渡す。
	//
	// ■ 複数登録できる(今回は "/" 1つだけ)
	//   http.HandleFunc("/", helloHandler)
	//   http.HandleFunc("/about", aboutHandler)
	//   http.HandleFunc("/contact", contactHandler)
	//   → 案内板が増えていく
	//
	// ■ "/" は "何にもマッチしなかったもの全部" にヒットする
	//   他のパスを登録しない限り、/anywhere や /deep/nested も
	//   この helloHandler に来る(今日実験で確認した挙動)
	http.HandleFunc("/", helloHandler)

	// =============================================================
	// 人間向けの起動メッセージ
	// =============================================================
	// ターミナルに出す案内。起動したか/URLは何番か、を人間が読むため。
	// ブラウザには関係ない(ターミナル専用)。
	fmt.Println("サーバー起動中: http://localhost:8080")

	// =============================================================
	// D. 営業開始: http.ListenAndServe
	// =============================================================
	//
	// 「営業開始!」の号令。8080番ポートで待機状態に入る。
	//
	// ■ 【重要】この行は戻ってこない!
	//   → 中で無限ループに入り、お客さんを永遠に受け付け続ける
	//   → プログラムを止めるには Ctrl+C
	//   → サーバーというものの本質: 自然には終わらない
	//
	// ■ 引数の分解
	//
	//   ":8080" = 受付窓口のポート番号
	//     先頭の : = 「どのIPからでも受け付ける」の意味
	//     8080    = 慣習的に開発用Webで使う番号
	//               (本番だと :80(HTTP) や :443(HTTPS) が多い)
	//
	//   nil     = 「デフォルトの案内板(HandleFuncで登録した表)を使う」
	//     nil は「空っぽ/指定しない」の意味(Loop 3 で出てきたやつ)
	//     自前の案内板を作って渡すこともできるが、今は標準で十分
	//
	// ■ ポート番号って何?
	//   パソコン = マンション全体
	//   ポート   = 部屋番号
	//   localhost:8080 = 「このマンションの8080号室」
	//   → 同じポートは2つのプログラムで同時に使えない(部屋は1人に1つ)
	//
	// ■ 中で起きている無限ループ(概念)
	//   for {
	//     ① 接続を待つ(お客さんが来るまで待機)
	//     ② リクエストを読む(申込書を受け取る)
	//     ③ 案内板を見て、対応する係員を呼ぶ
	//     ④ 係員が書いた返信用紙を送る
	//     ⑤ ①に戻る
	//   }
	http.ListenAndServe(":8080", nil)
}

// =============================================================================
// 全部つなげた流れ図
// =============================================================================
//
// main 開始
//   │
//   ▼
// [案内板に登録]
//   http.HandleFunc("/", helloHandler)
//   │
//   ▼
// [人間向けメッセージ]
//   fmt.Println("サーバー起動中: ...")
//   │
//   ▼
// [営業開始 → 無限ループ]
//   http.ListenAndServe(":8080", nil)
//   │
//   └─→ ⟲ 以下を無限に繰り返す ⟲
//          │
//          ▼
//       お客さん到来(ブラウザからのリクエスト)
//          │
//          ▼
//       Goが案内板を見る
//          │
//          ▼
//       helloHandler(w, r) を呼ぶ
//          │
//          └─ fmt.Fprintln(w, "Hello, ご主人様!")
//             w に書き込み
//          │
//          ▼
//       Goが w の内容をレスポンスとして送信
//          │
//          ▼
//       ブラウザに "Hello, ご主人様!" が表示
//          │
//          ▼
//       次のお客さんを待つ
